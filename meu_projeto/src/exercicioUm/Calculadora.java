package exercicioUm;

public class Calculadora {

    public int soma(int num, int num2) {
        int resultado = num + num2;
        return resultado;

    }
    public int subtrai(int num, int num2) {
        int resultado = num - num2;
        return resultado;
    }
    public int multiplica(int num, int num2) {
        int resultado = num * num2;
        return resultado;
    }
    public int divide(int num, int num2) {
        int resultado = num / num2;
        return resultado;
    }




}


